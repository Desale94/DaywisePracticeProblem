read -p "Enter a number: " n
if [ $n -eq 0 ]
then
   echo $n" :This number is Zero"
elif [ $n -eq 1 ]
then
  echo $n" :This number is One"
elif [ $n -eq 2 ]
then
  echo $n" :This number is Two"
elif [ $n -eq 3 ]
then
  echo $n" :This number is Three"
elif [ $n -eq 4 ]
then
  echo $n" :This number is Four"
elif [ $n -eq 5 ]
then
  echo $n" :This number is Five"
elif [ $n -eq 6 ]
then
  echo $n" :This number is Six"
  elif [ $n -eq 7 ]
then
  echo $n" :This number is Seven"
  elif [ $n -eq 8 ]
then
  echo $n" :This number is Eight"
  elif [ $n -eq 9 ]
then
  echo $n" :This number is Nine"
else
  echo "Wrong Input!!!!!"
fi
